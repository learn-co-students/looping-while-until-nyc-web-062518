def using_while
	levitation_force = 6
# where the variable is starting from
	while levitation_force < 10
		#will put out string below so long its true
		puts "Wingardium Leviosa"

	#string
		levitation_force += 1
end
end

def using_until
	levitation_force = 6
	until levitation_force == 10
		puts "Wingardium Leviosa"

		levitation_force += 1
end
end
