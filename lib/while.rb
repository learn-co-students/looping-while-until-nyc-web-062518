def using_while
	levitation_force = 6
# where the variable is starting from
	while levitation_force < 10
		#will put out string until false
		puts "Wingardium Leviosa"

	#string
		levitation_force += 1
end
end

def using_until
	levitation_force = 6
# set the variable to a number
	until levitation_force == 10
# will run until this is true

		puts "Wingardium Leviosa"

	# what is being returned

		levitation_force += 1
#how it is incrementing

end

#ends the until construct

end

#ends the method
